class CustomCNN(nn.Module):

    def __init__(self, num_classes=2):
        super(CustomCNN, self).__init__()

        self.features = nn.Sequential(
            # Block 1: 3 → 32  (224 → 112)
            nn.Conv2d(3, 32, kernel_size=3, padding=1),
            nn.BatchNorm2d(32),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),

            # Block 2: 32 → 64  (112 → 56)
            nn.Conv2d(32, 64, kernel_size=3, padding=1),
            nn.BatchNorm2d(64),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),

            # Block 3: 64 → 128  (56 → 28)
            nn.Conv2d(64, 128, kernel_size=3, padding=1),
            nn.BatchNorm2d(128),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),

            # Block 4: 128 → 256  (28 → 14)
            nn.Conv2d(128, 256, kernel_size=3, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),
        )

        self.gap = nn.AdaptiveAvgPool2d(1)

        self.classifier = nn.Sequential(
            nn.Flatten(),
            nn.Linear(256, 128),
            nn.ReLU(inplace=True),
            nn.Dropout(0.5),
            nn.Linear(128, num_classes),
        )

    def forward(self, x):
        x = self.features(x)
        x = self.gap(x)
        x = self.classifier(x)
        return x

"""**Quality-Aware CNN**"""

class QualityAwareCNN(nn.Module):

    def __init__(self, num_classes=2):
        super(QualityAwareCNN, self).__init__()

        self.features = nn.Sequential(
            nn.Conv2d(3, 32, kernel_size=3, padding=1),
            nn.BatchNorm2d(32),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(32, 64, kernel_size=3, padding=1),
            nn.BatchNorm2d(64),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(64, 128, kernel_size=3, padding=1),
            nn.BatchNorm2d(128),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(128, 256, kernel_size=3, padding=1),
            nn.BatchNorm2d(256),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),
        )

        self.gap = nn.AdaptiveAvgPool2d(1)

        #+1 karena quality score di-concat ke feature vector
        self.classifier = nn.Sequential(
            nn.Linear(256 + 1, 128),
            nn.ReLU(inplace=True),
            nn.Dropout(0.5),
            nn.Linear(128, num_classes),
        )

    def forward(self, x, quality):
        x = self.features(x)
        x = self.gap(x)
        x = x.view(x.size(0), -1)
        q = quality.unsqueeze(1)
        x = torch.cat([x, q], dim=1)
        x = self.classifier(x)
        return x

"""**Training & Evaluation Functions**"""

def train_one_epoch(model, loader, optimizer, criterion, device, use_quality=False):
    model.train()
    total_loss, correct, total = 0, 0, 0
    for batch in tqdm(loader, desc="  Training", leave=False):
        if use_quality:
            imgs, lbl, quality = batch
            quality = quality.to(device)
        else:
            imgs, lbl = batch
        imgs, lbl = imgs.to(device), lbl.to(device)

        optimizer.zero_grad()
        outputs = model(imgs, quality) if use_quality else model(imgs)
        loss = criterion(outputs, lbl)
        loss.backward()
        optimizer.step()

        total_loss += loss.item() * imgs.size(0)
        correct    += (outputs.argmax(1) == lbl).sum().item()
        total      += imgs.size(0)

    return total_loss / total, correct / total


def evaluate(model, loader, criterion, device, use_quality=False):
    model.eval()
    total_loss, correct, total = 0, 0, 0
    all_preds, all_probs, all_labels = [], [], []

    with torch.no_grad():
        for batch in loader:
            if use_quality:
                imgs, lbl, quality = batch
                quality = quality.to(device)
            else:
                imgs, lbl = batch
            imgs, lbl = imgs.to(device), lbl.to(device)

            outputs = model(imgs, quality) if use_quality else model(imgs)
            loss    = criterion(outputs, lbl)

            probs = torch.softmax(outputs, dim=1)[:, 1]
            preds = outputs.argmax(1)

            total_loss += loss.item() * imgs.size(0)
            correct    += (preds == lbl).sum().item()
            total      += imgs.size(0)

            all_preds.extend(preds.cpu().numpy())
            all_probs.extend(probs.cpu().numpy())
            all_labels.extend(lbl.cpu().numpy())

    return (total_loss / total, correct / total,
            np.array(all_labels), np.array(all_preds), np.array(all_probs))


def compute_metrics(y_true, y_pred, y_prob):
    tn, fp, fn, tp = confusion_matrix(y_true, y_pred).ravel()
    return {
        "Accuracy"    : accuracy_score(y_true, y_pred),
        "Precision"   : precision_score(y_true, y_pred, zero_division=0),
        "Recall/Sens" : recall_score(y_true, y_pred, zero_division=0),
        "Specificity" : tn / (tn + fp) if (tn + fp) > 0 else 0,
        "F1-Score"    : f1_score(y_true, y_pred, zero_division=0),
        "ROC-AUC"     : roc_auc_score(y_true, y_prob),
    }


def train_model(model, train_loader, val_loader, optimizer, criterion,
                scheduler, num_epochs, device, use_quality=False,
                save_path="best_model.pth", patience=10):
    best_val_loss    = float("inf")
    patience_counter = 0
    history = {"train_loss": [], "val_loss": [], "train_acc": [], "val_acc": []}

    for epoch in range(1, num_epochs + 1):
        tr_loss, tr_acc = train_one_epoch(
            model, train_loader, optimizer, criterion, device, use_quality)
        vl_loss, vl_acc, _, _, _ = evaluate(
            model, val_loader, criterion, device, use_quality)

        scheduler.step(vl_loss)
        history["train_loss"].append(tr_loss)
        history["val_loss"].append(vl_loss)
        history["train_acc"].append(tr_acc)
        history["val_acc"].append(vl_acc)

        print(f"Epoch [{epoch:3d}/{num_epochs}] "
              f"Train Loss: {tr_loss:.4f} Acc: {tr_acc:.4f} | "
              f"Val Loss: {vl_loss:.4f} Acc: {vl_acc:.4f}")

        if vl_loss < best_val_loss:
            best_val_loss    = vl_loss
            patience_counter = 0
            torch.save(model.state_dict(), save_path)
        else:
            patience_counter += 1
            if patience_counter >= patience:
                print(f"⏹ Early stopping di epoch {epoch}")
                break

    return history


