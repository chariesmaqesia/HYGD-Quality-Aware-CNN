class GlaucomaDataset(Dataset):
    """Dataset class untuk HYGD — pakai variabel 'labels' (DataFrame)."""

    def __init__(self, dataframe, transform=None, use_quality=False):
        self.df          = dataframe.reset_index(drop=True)
        self.transform   = transform
        self.use_quality = use_quality

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        img = Image.open(row["image_path"]).convert("RGB")
        if self.transform:
            img = self.transform(img)
        label = torch.tensor(row["label_bin"], dtype=torch.long)
        if self.use_quality:
            quality = torch.tensor(row["quality_score"] / 10.0, dtype=torch.float32)
            return img, label, quality
        return img, label

