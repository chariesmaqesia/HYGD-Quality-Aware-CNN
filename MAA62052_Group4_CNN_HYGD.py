def get_quality_tier_metrics(df_test_set, y_true, y_pred, y_prob):
    "Hitung metrics per tier kualitas untuk SATU model. Return dict {tier_name: metrics_dict_or_None}."
    df_eval = df_test_set.copy()
    df_eval["y_true"] = y_true
    df_eval["y_pred"] = y_pred
    df_eval["y_prob"] = y_prob

    tiers = {
        "Low (1–3)":    df_eval["quality_score"] <= 3,
        "Medium (4–6)": (df_eval["quality_score"] >= 4) & (df_eval["quality_score"] <= 6),
        "High (7–10)":  df_eval["quality_score"] >= 7,
    }

    results = {}
    for tier_name, mask in tiers.items():
        subset = df_eval[mask]
        if len(subset) < 2 or subset["y_true"].nunique() < 2:
            results[tier_name] = {"n": len(subset), "metrics": None}
            continue
        m = compute_metrics(subset["y_true"].values,
                            subset["y_pred"].values,
                            subset["y_prob"].values)
        results[tier_name] = {"n": len(subset), "metrics": m}
    return results


